package com.abs.scholarship.dto;

public class MailRequest {

	private String toMail;
	
	private Boolean isFirst;
	
	private String studentName;
	
	private String email;
	
	private String number;
	
	private String address;
	
	private String interestedCourse;
	
	private String fathersNumber;
	
	private String fathersOccupation;
	
	private String fathersAnnualIncome;

	public String getToMail() {
		return toMail;
	}

	public void setToMail(String toMail) {
		this.toMail = toMail;
	}

	public Boolean getIsFirst() {
		return isFirst;
	}

	public void setIsFirst(Boolean isFirst) {
		this.isFirst = isFirst;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getInterestedCourse() {
		return interestedCourse;
	}

	public void setInterestedCourse(String interestedCourse) {
		this.interestedCourse = interestedCourse;
	}

	public String getFathersNumber() {
		return fathersNumber;
	}

	public void setFathersNumber(String fathersNumber) {
		this.fathersNumber = fathersNumber;
	}

	public String getFathersOccupation() {
		return fathersOccupation;
	}

	public void setFathersOccupation(String fathersOccupation) {
		this.fathersOccupation = fathersOccupation;
	}

	public String getFathersAnnualIncome() {
		return fathersAnnualIncome;
	}

	public void setFathersAnnualIncome(String fathersAnnualIncome) {
		this.fathersAnnualIncome = fathersAnnualIncome;
	}
	

}
